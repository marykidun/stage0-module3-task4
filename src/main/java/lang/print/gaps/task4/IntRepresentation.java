public class IntRepresentation {
    public static void main(String[] args) {
        char c = 'c';
        c = 99;
        char h = 'h';
        h = 104;
        char a = 'a';
        a = 97;
        char r = 'r';
        r = 114;

        System.out.println(c);
        System.out.println(h);
        System.out.println(a);
        System.out.println(r);

    }
}

